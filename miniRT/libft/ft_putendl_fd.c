#include "libft.h"

int	ft_putendl_fd(char *s, int fd)
{
	int	len;

	len = ft_strlen(s) + 1;
	while (*s)
	{
		write(fd, s, 1);
		s++;
	}
	write(fd, "\n", 1);
	return (len);
}
